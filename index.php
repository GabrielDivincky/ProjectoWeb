	<script>
		
		$(document).ready(function(){

			$('#bEnviar').click(function(){
				var nombre = $('#itNombre').val();// val : significa el valor que haya en el input itNombre
				var correo = $('#itMail').val();
				var asunto = $('#itAsunto').val();


				if (nombre == "") {
					$('#itNombre').css('border-bottom', '2px solid red');
					return false; // return false significa que si nombre esta vacio, que cumpla la condicion del borde rojo y que imposibilite enviar, cuando se aprete el boton pedir ppto. 
				}else{
					$('#itNombre').css('border-bottom', '1px solid #90CAF9');
				


				if (correo == "") {
					$('#itMail').css('border-bottom', '2px solid red');
					return false;
				}else{
					$('#itMail').css('border-bottom', '1px solid #90CAF9');
				


				if (asunto == "") {
					$('#itAsunto').css('border-bottom', '2px solid red');
					return false;
				}
			}
				$('#bEnviar').fadeOut(); // Oculta el boton pedir presupuesto
				$('#mensaje4').fadeIn(); // Aparece el cartel mensaje enviado		
		
			}
		})

	})		
	</script>

